    private void calcularCambio() {
    if (!txtPaga.getText().isEmpty()) {
        double paga = Double.parseDouble(txtPaga.getText());
        double total = Double.parseDouble(txtTotalPagar.getText());
        txtCambio.setText(String.format("%.2f", paga - total));
    }
}
    
    private HashMap<String, Integer> metodosPagoMap = new HashMap<>();
    private void cargarMetodosPago() {
    try {
        String sql = "SELECT ID, Pago FROM metodo_pago";
        PreparedStatement pst = conexion.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        boxMetodoPago.removeAllItems();
        metodosPagoMap.clear();

        while (rs.next()) {
            int idPago = rs.getInt("ID");
            String nombre = rs.getString("Pago");
            boxMetodoPago.addItem(nombre);
            metodosPagoMap.put(nombre, idPago); // Guardar la relaciÃ³n Nombre -> ID
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
}


